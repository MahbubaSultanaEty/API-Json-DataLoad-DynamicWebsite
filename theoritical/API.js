/*API (Application Programming Interface) – Definition

An API is a set of rules and protocols that allows one software application to communicate with another.
It acts as a bridge between systems, enabling them to request and exchange data or services without knowing each other’s internal details.

In Simple Words

An API lets different programs talk to each other and share data or functionality in a controlled way.

Key Points (for notes)

API = Application Programming Interface

Used for communication between software systems

Commonly used between frontend and backend

Data is usually exchanged in JSON format */